public class Plano2d {
    private double dimensiones;
    private String cosCarroCompras;

    public Plano2d(){}

    public double getDimensiones() {
        return dimensiones;
    }

    public void setDimensiones(double dimensiones) {
        this.dimensiones = dimensiones;
    }

    public String getCosCarroCompras() {
        return cosCarroCompras;
    }

    public void setCosCarroCompras(String cosCarroCompras) {
        this.cosCarroCompras = cosCarroCompras;
    }
}
