class Usuario {

    private int usrDni;
    private String usrNombre;
    private String usrApellidos;
    private String usrPassword;
    private String usrMail;
    private String usrCodigo;

    public void setUsrDni(int usrDni) {
        this.usrDni = usrDni;
    }

    public void setUsrNombre(String usrNombre) {
        this.usrNombre = usrNombre;
    }

    public void setUsrApellidos(String usrApellidos) {
        this.usrApellidos = usrApellidos;
    }

    public void setUsrPassword(String usrPassword) {
        this.usrPassword = usrPassword;
    }

    public void setUsrMail(String usrMail) {
        this.usrMail = usrMail;
    }

    public void setUsrCodigo(String usrCodigo) {
        this.usrCodigo = usrCodigo;
    }

    public int getUsrDni() {
        return usrDni;
    }

    public String getUsrNombre() {
        return usrNombre;
    }

    public String getUsrApellidos() {
        return usrApellidos;
    }

    public String getUsrPassword() {
        return usrPassword;
    }

    public String getUsrMail() {
        return usrMail;
    }

    public String getUsrCodigo() {
        return usrCodigo;
    }
}